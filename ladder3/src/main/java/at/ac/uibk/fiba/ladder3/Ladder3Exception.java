package at.ac.uibk.fiba.ladder3;

public class Ladder3Exception extends Exception {

    public Ladder3Exception() {
    }

    public Ladder3Exception(String message) {
        super(message);
    }

    public Ladder3Exception(String message, Throwable cause) {
        super(message, cause);
    }

    public Ladder3Exception(Throwable cause) {
        super(cause);
    }
}
